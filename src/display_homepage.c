void render_homepage() {
	SSD1306_Fill(SSD1306_COLOR_BLACK);

	if (RELAY_STATE == RELAY_OFF) {
		SSD1306_drawBitmap(20,5, relay_on_32_53, 27, 53, SSD1306_COLOR_WHITE);
	}else{
		SSD1306_drawBitmap(30,5, relay_off_32_53, 27, 53, SSD1306_COLOR_WHITE);
	}
	SSD1306_UpdateScreen();
}

void Home_Page_Unregister(){
	// free memory here
}

void controller_homepage(int btn) {
	if (btn == -1){
		Home_Page_Unregister();
		return;
	}
	if ( btn & BTN4 ){
			Display_Set_Active_Controller(controller_main_menu);
			Display_Set_Active_Render(render_main_menu);
			Main_Menu_Init();
	}
}
